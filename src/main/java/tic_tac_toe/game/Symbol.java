package tic_tac_toe.game;

/**
 * Represents the possible symbols used in the game of tic-tac-toe
 */
public enum Symbol {
    /** Represents the 'X' symbol in the game */
    X,

    /** Represents the 'O' symbol in the game */
    O,

    /** Represents the absence of a symbol (empty cell or no winner) */
    NONE
}

